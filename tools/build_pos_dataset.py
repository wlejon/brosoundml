#!/usr/bin/env python3
"""Build pos_train.bin / pos_val.bin and regenerate include/brosoundml/g2p/tags.h
from a local Universal Dependencies checkout. NOT part of the C++ build.

Usage:
    python tools/build_pos_dataset.py \
        --ud-root <path containing UD_English-EWT/, UD_English-GUM/, ...> \
        --out-dir <path to write pos_train.bin + pos_val.bin> \
        --tags-out include/brosoundml/g2p/tags.h \
        [--treebanks EWT,GUM,Atis,Pronouns,PUD] \
        [--val-frac 0.05] [--seed 42]

If a UD checkout isn't available, the existing chunk-1 placeholder tags.h is
fine — the C++ build picks NUM_TAGS from whatever header is present. Running
this script overwrites tags.h with the inventory observed in the data.
"""

import argparse
import glob
import os
import random
import re
import struct
import sys

MAGIC = 0x504F5302  # "POS\x02" — v2 adds a uint8 UPOS id block per sentence.
MAX_SEQ_LEN = 384

# Universal Dependencies v2 universal POS tag inventory (17 tags). Fixed —
# the C++ side hardcodes the same order and never re-derives it from data.
UPOS_TAGS = [
    "ADJ", "ADP", "ADV", "AUX", "CCONJ", "DET", "INTJ", "NOUN", "NUM",
    "PART", "PRON", "PROPN", "PUNCT", "SCONJ", "SYM", "VERB", "X",
]
UPOS_TO_ID = {t: i for i, t in enumerate(UPOS_TAGS)}


def parse_conllu(path):
    """Yield (forms, xpos, upos) tuples, one per sentence. Skip sentences with
    any missing XPOS or UPOS. Multiword tokens (ID with '-') and empty nodes
    (ID with '.') are skipped."""
    forms, xtags, utags = [], [], []
    with open(path, encoding="utf-8") as f:
        for line in f:
            line = line.rstrip("\n")
            if not line:
                if forms:
                    yield forms, xtags, utags
                    forms, xtags, utags = [], [], []
                continue
            if line.startswith("#"):
                continue
            cols = line.split("\t")
            if len(cols) < 5:
                continue
            tok_id = cols[0]
            if "-" in tok_id or "." in tok_id:
                continue
            form, upos, xpos = cols[1], cols[3], cols[4]
            forms.append(form)
            xtags.append(xpos)
            utags.append(upos)
        if forms:
            yield forms, xtags, utags


def tokenised_len(forms):
    """Replicate chunk 1's tokeniser length math:
       1 (<bos>) + sum_w(1 + len(form_utf8)) + 1 (<eos>)."""
    total = 2  # bos + eos
    for f in forms:
        total += 1 + len(f.encode("utf-8"))
    return total


def split_long(forms, xtags, utags):
    """Split a sentence at word boundaries so each piece fits MAX_SEQ_LEN
    under the chunk 1 tokeniser. Also caps the joined byte buffer at 65535
    bytes (uint16 offsets in the binary format)."""
    out = []
    cur_f, cur_x, cur_u = [], [], []
    cur_tok = 2  # bos + eos baseline
    cur_bytes = 0  # joined byte buffer length
    for f, x, u in zip(forms, xtags, utags):
        b = len(f.encode("utf-8"))
        add_tok = 1 + b
        add_bytes = (1 if cur_f else 0) + b
        if cur_f and (cur_tok + add_tok > MAX_SEQ_LEN or cur_bytes + add_bytes > 65000):
            out.append((cur_f, cur_x, cur_u))
            cur_f, cur_x, cur_u, cur_tok, cur_bytes = [], [], [], 2, 0
            add_bytes = b
        cur_f.append(f)
        cur_x.append(x)
        cur_u.append(u)
        cur_tok += add_tok
        cur_bytes += add_bytes
    if cur_f:
        if cur_tok <= MAX_SEQ_LEN and cur_bytes <= 65000:
            out.append((cur_f, cur_x, cur_u))
    return out


def sanitize_enum(xpos):
    """Map an XPOS string to a valid C++ enum identifier."""
    name_map = {
        ".": "P_DOT", ",": "P_COMMA", ":": "P_COLON", ";": "P_SEMI",
        "``": "P_BACKTICK_BACKTICK", "''": "P_QUOTE_QUOTE",
        "-LRB-": "P_LRB", "-RRB-": "P_RRB",
        "$": "P_DOLLAR", "#": "P_HASH",
    }
    if xpos in name_map:
        return name_map[xpos]
    s = re.sub(r"[^A-Za-z0-9_]", "_", xpos)
    if not s:
        s = "EMPTY"
    if not s[0].isalpha() and s[0] != "_":
        s = "P_" + s
    return s


def write_tags_header(path, tags, treebanks):
    enum_names = []
    seen = set()
    for t in tags:
        en = sanitize_enum(t)
        base = en
        i = 2
        while en in seen:
            en = f"{base}_{i}"
            i += 1
        seen.add(en)
        enum_names.append(en)

    n = len(tags)
    lines = [
        f"// GENERATED by tools/build_pos_dataset.py — do not edit.",
        f"// Tag inventory derived from UD treebanks: {','.join(treebanks)}",
        f"#pragma once",
        f"#include <cstdint>",
        f"",
        f"namespace brosoundml::g2p {{",
        f"",
        f"enum class PosTag : std::uint8_t {{",
    ]
    for en, raw in zip(enum_names, tags):
        lines.append(f"    {en},  // {raw}")
    lines.append("};")
    lines.append("")
    lines.append(f"inline constexpr int NUM_TAGS = {n};")
    lines.append("")
    lines.append(f"inline constexpr const char* kPosTagNames[NUM_TAGS] = {{")
    for raw in tags:
        esc = raw.replace("\\", "\\\\").replace("\"", "\\\"")
        lines.append(f"    \"{esc}\",")
    lines.append("};")
    lines.append("")
    lines.append("// Universal Dependencies v2 UPOS — fixed inventory, 17 tags.")
    lines.append("enum class UPosTag : std::uint8_t {")
    for u in UPOS_TAGS:
        lines.append(f"    {u},")
    lines.append("};")
    lines.append("")
    lines.append(f"inline constexpr std::uint32_t NUM_UPOS_TAGS = {len(UPOS_TAGS)};")
    lines.append("")
    lines.append("inline constexpr const char* kUPosTagNames[NUM_UPOS_TAGS] = {")
    for u in UPOS_TAGS:
        lines.append(f"    \"{u}\",")
    lines.append("};")
    lines.append("")
    lines.append("}  // namespace brosoundml::g2p")
    lines.append("")
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))


def serialize_sentences(path, sentences, tag_to_id):
    """Binary layout v2 (magic 0x504F5302):
       [u32 magic][u32 num_xpos_tags][u32 num_sentences]
       per sentence:
         [u32 num_bytes][bytes...]
         [u32 num_words]
         [u8  xpos_id  × num_words]
         [u8  upos_id  × num_words]     ← new in v2
         [u16 word_byte_start × num_words]
         [u16 word_byte_len   × num_words]
    """
    with open(path, "wb") as f:
        f.write(struct.pack("<I", MAGIC))
        f.write(struct.pack("<I", len(tag_to_id)))
        f.write(struct.pack("<I", len(sentences)))
        for forms, xtags, utags in sentences:
            joined = " ".join(forms).encode("utf-8")
            offsets = []
            off = 0
            for w in forms:
                wb = w.encode("utf-8")
                offsets.append((off, len(wb)))
                off += len(wb) + 1  # +1 for space separator
            f.write(struct.pack("<I", len(joined)))
            f.write(joined)
            f.write(struct.pack("<I", len(forms)))
            for t in xtags:
                f.write(struct.pack("<B", tag_to_id[t]))
            for u in utags:
                f.write(struct.pack("<B", UPOS_TO_ID[u]))
            for start, _ in offsets:
                f.write(struct.pack("<H", start))
            for _, ln in offsets:
                f.write(struct.pack("<H", ln))


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--ud-root", required=True)
    ap.add_argument("--out-dir", required=True)
    ap.add_argument("--tags-out", required=True)
    ap.add_argument("--treebanks", default="EWT,GUM")
    ap.add_argument("--val-frac", type=float, default=0.05)
    ap.add_argument("--seed", type=int, default=42)
    args = ap.parse_args()

    treebanks = [t.strip() for t in args.treebanks.split(",") if t.strip()]
    all_sentences = []
    dropped = 0

    for tb in treebanks:
        tb_dir = os.path.join(args.ud_root, f"UD_English-{tb}")
        files = sorted(glob.glob(os.path.join(tb_dir, "*.conllu")))
        if not files:
            print(f"warn: no .conllu files under {tb_dir}", file=sys.stderr)
            continue
        for fp in files:
            for forms, xtags, utags in parse_conllu(fp):
                if any(t == "_" or not t for t in xtags):
                    dropped += 1
                    continue
                if any(t == "_" or not t for t in utags):
                    dropped += 1
                    continue
                if any(t not in UPOS_TO_ID for t in utags):
                    dropped += 1
                    continue
                pieces = split_long(forms, xtags, utags)
                if not pieces:
                    dropped += 1
                    continue
                for f, x, u in pieces:
                    all_sentences.append((f, x, u))

    tag_set = sorted({t for _, xs, _ in all_sentences for t in xs})
    tag_to_id = {t: i for i, t in enumerate(tag_set)}

    max_tok = 0
    for forms, _, _ in all_sentences:
        l = tokenised_len(forms)
        if l > max_tok:
            max_tok = l

    rng = random.Random(args.seed)
    rng.shuffle(all_sentences)
    n_val = max(1, int(len(all_sentences) * args.val_frac))
    val = all_sentences[-n_val:]
    train = all_sentences[:-n_val]

    os.makedirs(args.out_dir, exist_ok=True)
    serialize_sentences(os.path.join(args.out_dir, "pos_train.bin"), train, tag_to_id)
    serialize_sentences(os.path.join(args.out_dir, "pos_val.bin"), val, tag_to_id)
    write_tags_header(args.tags_out, tag_set, treebanks)

    print(f"tags: {len(tag_set)}")
    print(f"train sentences: {len(train)}")
    print(f"val sentences:   {len(val)}")
    print(f"max tokenised length: {max_tok}")
    print(f"dropped: {dropped}")


if __name__ == "__main__":
    main()
